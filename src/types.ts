/**
 * Options
 */
export interface PanelOptions {
  /**
   * Field name for image
   *
   * @type string
   */
  name: string;

  /**
   * Field name for image width
   *
   * @type string
   */
  widthName: string;

  /**
   * Field name for image height
   *
   * @type string
   */
  heightName: string;

  /**
   * Image width
   *
   * @type number
   */
  width: number;

  /**
   * Image height
   *
   * @type number
   */
  height: number;
}
